    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        if (TXTUsuario.getText().equals("") || TXTClave.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Deben digitar el nombre de usuario y su contraseña.");
            TXTUsuario.setText("");
            TXTClave.setText("");
        } else {
            boolean res = tud.login(TXTUsuario.getText(), TXTClave.getText());
            if (res == true) {
                TblpersonaVista vista = new TblpersonaVista();
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "El nombre de usuario y/o la contraseña estan erroneas.");
            }
        }
    }//GEN-LAST:event_btnIniciarActionPerformed

